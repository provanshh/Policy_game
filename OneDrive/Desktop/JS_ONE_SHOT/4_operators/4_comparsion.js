/*  comparison operators : 
        - used to compare 2 values & give back a boolean value(true/false)
        - useful in decision making & loop program

        <   (less than)
        >   (greater than)
        <=  (less than or equal to)
        >=  (greater than or equal to)

        ==  (Equal checks )
        !=  (inequality) (not equal) (flipped value of equal checks)
        === (strict equality checks ) (checks the Data type also)
        !== (strict inequality (!==) (flipped value of strict equality checks)
*/

let a = 10
let b = 20

console.log(a < b)
console.log(a > b)
console.log(a <= b)
console.log(a >= b)
console.log('-------------------')

console.log(a == b)
console.log(a != b)
console.log('-------------------')

console.log(a === b)
console.log(a !== b)
console.log('-------------------')


