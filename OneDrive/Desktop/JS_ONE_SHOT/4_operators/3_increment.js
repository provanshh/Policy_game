/*  increment/decrement operators : 
        - used to inc or dec value of a variable by 1
        - increment operator is ++
        - decrement operator is --
        - can be used in 2 ways : prefix and postfix
*/


// prefix => first inc. then use
let a = 10
console.log(++a)
console.log(a)
console.log("--------------------------------")

// postfix => first use then inc.
let b = 20
console.log(b++)
console.log(b)
console.log("--------------------------------")

// prefix => first dec. then use
let c = 10
console.log(--c)
console.log(c)
console.log("--------------------------------")

// postfix => first use then dec.
let d = 20
console.log(d--)
console.log(d)
console.log("--------------------------------")
